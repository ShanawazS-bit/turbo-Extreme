#include<iostream>
#include<vector>

using namespace std;



class Solution {
public:
    void rotateArray(vector<int>& nums, int k) {
        int maxcount = 0;


        for (int i = 0; i < nums.size(); i++)
        {
           
            
            int count = 0;
            for (int j = i; j < nums.size(); j++)
            {
                while (count <= k)
                {
                    count += nums[j];
                }
                
                
            }
            

            

        }
        
    }
};


